/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t uartRecv[COM_READ_LEN] = {0};       
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  SCB->VTOR = APPLICATION_ADDRESS & (uint32_t)0x1FFFFF80;
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  LOG_INIT();
  LOG("QPCR STM32F302VET6 Application start...\r\n");
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    //HAL_Delay(500);
    HAL_GPIO_TogglePin(BLUE_D19_GPIO_Port, BLUE_D19_Pin);
    LOG("LED toggle...\r\n");
      
    if(HAL_OK == HAL_UART_Receive(&huart1, uartRecv, 12, 1000))     // waiting the update command
    {
        LOG("RecvData:\r\n");
        for(int i = 0; i < 12; i++) {
            LOG("D[%02d]=[0x%02x] ", i, uartRecv[i]);       // should be:D[00]=[0x55] D[01]=[0xAA] D[02]=[0x06] D[03]=[0x00] D[04]=[0x01] D[05]=[0x05] D[06]=[0x00] D[07]=[0x0B] D[08]=[0xE1] D[09]=[0x01] D[10]=[0x35] D[11]=[0x83]
        }
        LOG("\r\n");
        
        UpgradeDef stUpgradeInfo;
        stUpgradeInfo.UpdateType = uartRecv[9];     // UART_AUTO_UPGRADE
        JumpToIap(&stUpgradeInfo);  
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

